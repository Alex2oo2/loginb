    public void leerXML(File archivo) {
        try {
            DocumentBuilderFactory xml = DocumentBuilderFactory.newInstance();
            DocumentBuilder xm = xml.newDocumentBuilder();
            Document x = xm.parse(archivo);

            x.getDocumentElement().normalize();
            NodeList nodos = x.getElementsByTagName("alumno");
            for (int i = 0; i < nodos.getLength(); i++) {
                Node n = nodos.item(i);
                if (n.getNodeType() == Node.ELEMENT_NODE) {
                    Element e = (Element) n;
                    Alumno al = new Alumno();
                    al.carne = e.getElementsByTagName("carne")
                            .item(0).getTextContent();
                    al.nombre = e.getElementsByTagName("nombre")
                            .item(0).getTextContent();
                    al.apellido = e.getElementsByTagName("apellido")
                            .item(0).getTextContent();
                    al.password = e.getElementsByTagName("password")
                            .item(0).getTextContent();
                    Loginb.alumnos.add(al);
                }
            }
            actualizarDatos();
        } catch (Exception e) {

        }
    }

    public void guardarXML(String rutaConNombre, String contenido) {
        try (FileWriter fw = new FileWriter(rutaConNombre); PrintWriter pw = new PrintWriter(fw)) {
            pw.write(contenido);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al guardar el archivo XML.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


