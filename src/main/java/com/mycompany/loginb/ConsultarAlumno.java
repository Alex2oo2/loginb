    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        StringBuilder contenido = new StringBuilder("<alumnos>\n");

        for (Alumno alu : Loginb.alumnos) {
            contenido.append("\t<alumno>\n")
                    .append("\t\t<carne>").append(alu.getCarne()).append("</carne>\n")
                    .append("\t\t<nombre>").append(alu.getNombre()).append("</nombre>\n")
                    .append("\t\t<apellido>").append(alu.getApellido()).append("</apellido>\n")
                    .append("\t\t<password>").append(alu.getPassword()).append("</password>\n")
                    .append("\t</alumno>\n");
        }
        contenido.append("</alumnos>");

        JFileChooser archivo = new JFileChooser();
        int seleccionado = archivo.showSaveDialog(this);
        if (seleccionado == JFileChooser.APPROVE_OPTION) {
            guardarXML(archivo.getSelectedFile().toPath().toString(), contenido.toString());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

