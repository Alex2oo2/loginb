    public ModificarCurso(int posicion) {
        initComponents();
        this.setLocationRelativeTo(null);
        Curso cursoSeleccionado = Loginb.cursos.get(posicion);
        jTextField2.setText(cursoSeleccionado.getnombreCurso());
        jTextField3.setText(cursoSeleccionado.getSeccionCurso());

        String fechaInicioString = cursoSeleccionado.getFechaInicioCurso();
        String fechaFinString = cursoSeleccionado.getFechaFinCurso();

        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date fechaInicio = null;
        Date fechaFin = null;
        try {
            fechaInicio = dateFormat.parse(fechaInicioString);
            fechaFin = dateFormat.parse(fechaFinString);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        jDateChooser1.setDate(fechaInicio);
        jDateChooser2.setDate(fechaFin);

        jTextField4.setText(cursoSeleccionado.getHoraInicioCurso());
        jTextField5.setText(cursoSeleccionado.getHoraFinCurso());

        llenarComboBoxProfesores();
        jComboBox1.setSelectedItem(cursoSeleccionado.getProfesorSeleccionado());

        this.posicion = posicion;
    }

    private void llenarComboBoxProfesores() {
        ArrayList<Profesor> listaProfesores = Loginb.obtenerProfesores();
        jComboBox1.removeAllItems();
        for (Profesor profesor : listaProfesores) {
            jComboBox1.addItem(profesor.getUsuario());

        }

    }

