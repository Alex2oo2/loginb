    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nombreCurso = jTextField2.getText();
        String seccionCurso = jTextField3.getText();
        Date fechaInicioCurso = jDateChooser1.getDate();
        Date fechaFinCurso = jDateChooser2.getDate();
        String horaInicioCurso = jTextField4.getText();
        String horaFinCurso = jTextField5.getText();
        String profesorSeleccionado = (String) jComboBox1.getSelectedItem();

        if (nombreCurso.isEmpty() || seccionCurso.isEmpty() || fechaInicioCurso == null || fechaFinCurso == null || horaInicioCurso.isEmpty() || horaFinCurso.isEmpty() || profesorSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa todos los campos obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Curso cursoSeleccionado = Loginb.cursos.get(posicion);
        cursoSeleccionado.nombreCurso = nombreCurso;
        cursoSeleccionado.seccionCurso = seccionCurso;
        cursoSeleccionado.fechaInicioCurso = fechaInicioCurso;
        cursoSeleccionado.fechaFinCurso = fechaFinCurso;
        cursoSeleccionado.horaInicioCurso = horaInicioCurso;
        cursoSeleccionado.horaFinCurso = horaFinCurso;
        cursoSeleccionado.profesorSeleccionado = profesorSeleccionado;

        JOptionPane.showMessageDialog(this, "Curso modificado correctamente");
    }//GEN-LAST:event_jButton2ActionPerformed


