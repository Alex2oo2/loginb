    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nombreCursoSeleccionado = jComboBox1.getSelectedItem().toString();

        Curso cursoSeleccionado = null;
        for (Curso curso : Loginb.cursos) {
            if (curso.getnombreCurso().equals(nombreCursoSeleccionado)) {
                cursoSeleccionado = curso;
                break;
            }
        }

        if (cursoSeleccionado != null) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            model.setColumnIdentifiers(new Object[]{"Carne", "Nombre", "Apellido", "Nota"});

            for (Alumno alumno : cursoSeleccionado.alumnos) {
                Object[] row = {alumno.getCarne(), alumno.getNombre(), alumno.getApellido(), alumno.getNota()};
                model.addRow(row);
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String nombreCursoSeleccionado = jComboBox1.getSelectedItem().toString();

        Curso cursoSeleccionado = null;
        for (Curso curso : Loginb.cursos) {
            if (curso.getnombreCurso().equals(nombreCursoSeleccionado)) {
                cursoSeleccionado = curso;
                break;
            }
        }

        String valornota = jTextField1.getText();

        int nota;
        try {
            nota = Integer.parseInt(valornota);
        } catch (NumberFormatException e) {
            System.err.println("La nota ingresada no es v√°lida");
            return;
        }

        int row = jTable1.getSelectedRow();
        String name = jTable1.getModel().getValueAt(row, 1).toString();

        for (Alumno s : cursoSeleccionado.alumnos) {
            if (s.nombre.equals(name)) {
                s.nota = nota;
                break;
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed


